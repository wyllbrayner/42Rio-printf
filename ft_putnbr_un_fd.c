#include "printf.h"

void	ft_putnbr_un_fd(unsigned long n, int fd)
{
	if (fd > 0)
	{
		if (n / 10)
			ft_putnbr_un_fd((n / 10), fd);
		ft_putchar_fd(((n % 10) + '0'), fd);
	}
}
